p = 2
h =1
n = int(input())
c = n - 2
for i in range(0,n):
    if(i == (n//2)):
        print('*'*i+'X'+'*'*i)
    elif(i > ((n/2))):
        print('*'*(i-p)+'/'+"*"*h+'\\'+'*'*(i-p))
        h +=2
        p += 2
    else:    
       print('*'*i+'\\'+'*'*c+'/'+'*' * i)
       c -= 2
        